import Foundation

public extension TimeInterval {
    static let oneMinute: TimeInterval = 60
    static let fifteenMinutes: TimeInterval = 60 * 15
    static let halfAnHour: TimeInterval = 60 * 30
    static let oneHour: TimeInterval = 60 * 60
    static let sixHours: TimeInterval = 60 * 60 * 6
    static let halfADay: TimeInterval = 60 * 60 * 12
    static let oneDay: TimeInterval = 86400
    static let oneWeek: TimeInterval = 7 * 86400
}
